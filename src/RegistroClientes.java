    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        String nombreBuscado = txtBuscarNombre.getText().trim().toLowerCase();
    boolean encontrado = false;

    for (int i = 0; i < listaClientes.size(); i++) {
        Cliente cliente = listaClientes.get(i);
        if (cliente.getNombre().toLowerCase().equals(nombreBuscado)) {
            // Selecciona la fila en la tabla
            tblClientes.setRowSelectionInterval(i, i);

            // Muestra los datos en los campos de texto
            txtNombre.setText(cliente.getNombre());
            txtCedula.setText(cliente.getCedula());
            txtTelefono.setText(cliente.getTelefono());
            txtCorreo.setText(cliente.getCorreo());

            filaSeleccionada = i;
            encontrado = true;
            break;
        }
    }

    if (!encontrado) {
        javax.swing.JOptionPane.showMessageDialog(this, "Cliente no encontrado.");
        limpiarCampos();
    
    }
// TODO add your handling code here:
    }//GEN-LAST:event_btnBuscarActionPerformed

