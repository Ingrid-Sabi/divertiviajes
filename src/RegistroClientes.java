    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        String nombre = txtNombre.getText();
    String cedula = txtCedula.getText();
    String telefono = txtTelefono.getText();
    String correo = txtCorreo.getText();

    Cliente nuevo = new Cliente(nombre, cedula, telefono, correo);
    listaClientes.add(nuevo);
    
    modeloTabla.addRow(new Object[]{nombre, cedula, telefono, correo});
    limpiarCampos();
    
// TODO add your handling code here:
    }//GEN-LAST:event_btnRegistrarActionPerformed

    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
         filaSeleccionada = tblClientes.getSelectedRow();
    if (filaSeleccionada >= 0) {
        // Actualizar objeto en la lista
        Cliente cliente = listaClientes.get(filaSeleccionada);
        cliente.setNombre(txtNombre.getText());
        cliente.setCedula(txtCedula.getText());
        cliente.setTelefono(txtTelefono.getText());
        cliente.setCorreo(txtCorreo.getText());

        // Actualizar tabla
        modeloTabla.setValueAt(cliente.getNombre(), filaSeleccionada, 0);
        modeloTabla.setValueAt(cliente.getCedula(), filaSeleccionada, 1);
        modeloTabla.setValueAt(cliente.getTelefono(), filaSeleccionada, 2);
        modeloTabla.setValueAt(cliente.getCorreo(), filaSeleccionada, 3);

        limpiarCampos();
        filaSeleccionada = -1;
    }
// TODO add your handling code here:
    }//GEN-LAST:event_btnActualizarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
       filaSeleccionada = tblClientes.getSelectedRow();
    if (filaSeleccionada >= 0) {
        listaClientes.remove(filaSeleccionada); // Elimina de la lista
        modeloTabla.removeRow(filaSeleccionada); // Elimina de la tabla
        limpiarCampos();
        filaSeleccionada = -1;
    }
    
    }
    

    
    
    
